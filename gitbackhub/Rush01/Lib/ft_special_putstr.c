void	ft_putchar(char c);

void	ft_special_putstr(int *tab)
{
	int	i;

	i = 0;
	while (i <= 8)
	{
		ft_putchar(tab[i] + '0');
		if (i < 8)
			ft_putchar(' ');
		i++;
	}
}
