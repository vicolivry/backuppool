int	ft_atoi_modified(char *str)
{
	int i;
	int neg;
	int nb;

	i = 0;
	nb = 0;
	if (str[i] == '.')
		nb = -1;
	if (str[i] >= '0' && str[i] <= '9')
		nb = str[i] - '0';
	return (nb);
}
